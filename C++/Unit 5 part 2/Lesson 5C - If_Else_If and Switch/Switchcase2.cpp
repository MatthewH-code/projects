#include <iostream>
#include <string>
using namespace std;

int main()
{
	int num = 11;		
    switch (num)
    {
         case 11 :  cout << "num == 11" << endl;   
         case 22 :  cout << "num == 22" << endl;   
         case 30 :  cout << "num == 30" << endl;  
         case 40 :  cout << "num == 40" << endl;   
         default :  cout << "does not equal" << endl;

    }

	return 0;
}
