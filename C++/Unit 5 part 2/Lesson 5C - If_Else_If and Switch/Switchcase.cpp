#include <iostream>
#include <string>
using namespace std;

int main()
{
	int num = 15;		
    switch (num)
    {
      case 11 :  cout << "num == 11" << endl; break;  
      case 22 :  cout << "num == 22" << endl; break;  
      case 30 :  cout << "num == 30" << endl; break;  
      case 40 :  cout << "num == 40" << endl; break;  
      default :  cout << "does not equal" << endl;
    }

	return 0;
}
