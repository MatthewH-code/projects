#include <iostream>
#include <string>
using namespace std;

int main()
{
	int x = 10;
	int y = -10;
	 
	cout << "x>10 " << (x > 10) << endl;
	cout << "x<10 " << (x < 10) << endl;
	cout << "x>=10 " << (x >= 10) << endl;
	cout << "x<=10 " << (x <= 10) << endl;
	cout << "x == 10 " << (x == 10) << endl;
	cout << "x != 10 " << (x != 10) << endl;
	

	cout << "x+y == 0 " << (x+y == 0) << endl;
	
	
	
	return 0;
}
